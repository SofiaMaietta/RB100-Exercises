# What is exception handling and what problem does it solve?

# My answer: 

# Exception handling is a process of resolving errors that arise in a program's execution due to 
# outsude input from a user or from some other unpredictable factor.

# Exception handling uses the reserved words begin / rescue / end to rescue the program
# from the error and enable the program to continue running so that the program doesn't halt execution
# when the exception is raised. 

# Launch School answer:

# "Exception handling is a structure used to handle the possibility of an error occurring in a program."
# "It is a way of handling the error by changing the flow of control without exiting the program entirely."